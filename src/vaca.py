def vaca_diz(frase: str) -> None:
    """
    Exibe uma mensagem em um balão de fala acompanhado de uma arte ASCII de vaca.

    Esta função imprime a mensagem fornecida seguida de uma representação visual de uma vaca em ASCII art.

    Args:
        frase (str): Mensagem a ser exibida no balão de fala.
    """
    print(f"  {frase}")
    print("   \\   ^__^")
    print("    \\  (oo)\\_______")
    print("       (__)\\       )\\/\\")
    print("           ||----w |")
    print("           ||     ||")
